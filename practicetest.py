# Asks the user for two numbers.
# Prints the result of:
# Addition
# Subtraction
# Multiplication
# Division
# Modulus (remainder)

first = int(input("First_number?"))
second = int(input("Second_number?"))

print("add = ", first + second)
print("subs = ", first - second)
print("mul = ", first * second)
print("div = ", first / second)
print("mod = ", first % second)